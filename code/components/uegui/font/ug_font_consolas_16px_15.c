static int binsearch(const uint16_t *sortedSeq, int seqLength, uint16_t keyData) {
    int low = 0, mid, high = seqLength - 1;
    while (low <= high) {
        mid = (low + high) / 2;
        if (keyData < sortedSeq[mid]) {
            high = mid - 1;
        }
        else if (keyData > sortedSeq[mid]) {
            low = mid + 1;
        }
        else {
            return mid;
        }
    }
    return -1;
}


static const uint8_t * __user_font_get_bitmap(const ug_font_t * font, uint32_t unicode_letter) {
    ug_font_fmt_txt_dsc_t * fdsc = (ug_font_fmt_txt_dsc_t *) font->dsc;
    for(uint16_t cmapNum = 0; cmapNum < fdsc->cmap_num; cmapNum ++){
        if(unicode_letter < fdsc->cmaps[cmapNum].range_start || unicode_letter > (fdsc->cmaps[cmapNum].range_start + fdsc->cmaps[cmapNum].range_length - 1)){
            continue;
        }
        else{
            if(fdsc->cmaps[cmapNum].type == UG_FONT_FMT_TXT_CMAP_SPARSE_TINY){
                int i = binsearch(fdsc->cmaps[cmapNum].unicode_list, fdsc->cmaps[cmapNum].list_length, unicode_letter);
                if( i != -1 ) {
                    const ug_font_fmt_txt_glyph_dsc_t * gdsc = &fdsc->glyph_dsc[i + fdsc->cmaps[cmapNum].glyph_id_start];
                    return &fdsc->glyph_bitmap[gdsc->bitmap_index];
                }
            }
            else{
                const ug_font_fmt_txt_glyph_dsc_t * gdsc = &fdsc->glyph_dsc[unicode_letter - fdsc->cmaps[cmapNum].range_start + fdsc->cmaps[cmapNum].glyph_id_start];
                return &fdsc->glyph_bitmap[gdsc->bitmap_index];
            }
            return NULL;
        }
    }

    return NULL;
}


static bool __user_font_get_glyph_dsc(const ug_font_t * font, ug_font_glyph_dsc_t * dsc_out, uint32_t unicode_letter, uint32_t unicode_letter_next) {

    ug_font_fmt_txt_dsc_t * fdsc = (ug_font_fmt_txt_dsc_t *) font->dsc;

    for(uint16_t cmapNum = 0; cmapNum < fdsc->cmap_num; cmapNum ++){
        if(unicode_letter < fdsc->cmaps[cmapNum].range_start || unicode_letter > (fdsc->cmaps[cmapNum].range_start + fdsc->cmaps[cmapNum].range_length - 1)){
            continue;
        }
        else{
            if(fdsc->cmaps[cmapNum].type == UG_FONT_FMT_TXT_CMAP_SPARSE_TINY){
                int i = binsearch(fdsc->cmaps[cmapNum].unicode_list, fdsc->cmaps[cmapNum].list_length, unicode_letter);
                if( i != -1 ) {
                   const ug_font_fmt_txt_glyph_dsc_t * gdsc = &fdsc->glyph_dsc[i + fdsc->cmaps[cmapNum].glyph_id_start];
                   dsc_out->adv_w = gdsc->adv_w;
                   dsc_out->box_h = gdsc->box_h;
                   dsc_out->box_w = gdsc->box_w;
                   dsc_out->ofs_x = gdsc->ofs_x;
                   dsc_out->ofs_y = gdsc->ofs_y;
                   dsc_out->bpp   = fdsc->bpp;
                   return true;
                }

            }
            else{
                const ug_font_fmt_txt_glyph_dsc_t * gdsc = &fdsc->glyph_dsc[unicode_letter - fdsc->cmaps[cmapNum].range_start + fdsc->cmaps[cmapNum].glyph_id_start];
                dsc_out->adv_w = gdsc->adv_w;
                dsc_out->box_h = gdsc->box_h;
                dsc_out->box_w = gdsc->box_w;
                dsc_out->ofs_x = gdsc->ofs_x;
                dsc_out->ofs_y = gdsc->ofs_y;
                dsc_out->bpp   = fdsc->bpp;
                return true;
            }
            return false;
        }
    }

    return false;
}



